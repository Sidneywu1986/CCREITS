class PersonaRouter:
    """人设路由器"""

    def __init__(self):
        self.registry = PERSONA_REGISTRY

    def route(self, query: str, mentioned: Optional[str] = None) -> PersonaConfig:
        """
        路由逻辑：
        1. 用户@了谁 → 直接用（包括@片警、@朝阳群众）
        2. 没@ → 根据关键词匹配最适合的人设（飞行嘉宾不走关键词匹配，只走触发器/Supervisor调度）
        3. 都没命中 → 默认老K（因为他是主角）
        4. 关键词匹配分数低（只有1分）→ 仍默认老K，避免随便问个问题就切换角色
        """
        if mentioned:
            # 直接匹配英文ID
            if mentioned in self.registry:
                return self.registry[mentioned]
            # 匹配中文名（兼容前端可能传中文名的情况）
            for pid, cfg in self.registry.items():
                if cfg.name_cn == mentioned:
                    return cfg

        # 关键词匹配（飞行嘉宾不走关键词匹配，只走触发器/Supervisor调度）
        query_lower = query.lower()
        scores = {}
        for pid, cfg in self.registry.items():
            if pid == "standard" or cfg.guest_type != "regular":
                continue
            score = sum(1 for kw in cfg.trigger_keywords if kw in query_lower)
            scores[pid] = score

        best = max(scores, key=scores.get) if scores else "lao_k"
        best_score = scores.get(best, 0)

        # 默认主角老K：
        # - 没有任何关键词命中
        # - 最高分数只有1分（太弱，不足以切换角色）
        if best_score == 0 or best_score <= 1:
            best = "lao_k"

        return self.registry[best]

    def get_guest(self, guest_id: str) -> Optional[PersonaConfig]:
        """获取飞行嘉宾配置"""
        cfg = self.registry.get(guest_id)
        if cfg and cfg.guest_type.startswith("guest_"):
            return cfg
        return None

    def is_guest(self, persona_id: str) -> bool:
        return self.registry.get(persona_id, PersonaConfig("", "", "", 0, "", [], [])).guest_type.startswith("guest_")

    def build_prompt(
        self,
        persona_id: str,
        query: str,
        context: str,
        emotion_tag: Optional[str] = None,
    ) -> Dict:
        """
        组装完整 Prompt，注入情感标签和人格约束
        """
        cfg = self.registry.get(persona_id, self.registry["standard"])

        # 情感注入（让老K/苏苏根据市场情绪调整语气）
        emotion_instruction = ""
        if emotion_tag and persona_id == "lao_k":
            if emotion_tag in ["greed", "hope"]:
                emotion_instruction = "\n【今日市场情绪】偏热/贪婪 → 你的语气要更冷、更毒舌，专门挑风险说。"
            elif emotion_tag == "panic":
                emotion_instruction = "\n【今日市场情绪】恐慌 → 你的语气要稳，骂市场别骂用户，提醒资产基本面没变。"

        if emotion_tag and persona_id == "su_su":
            if emotion_tag in ["greed", "hope"]:
                emotion_instruction = "\n【今日市场情绪】偏热 → 用生活比喻降温，强调'好价格'的重要性。"
            elif emotion_tag == "panic":
                emotion_instruction = "\n【今日市场情绪】恐慌 → 语气要更温柔，用'阿拉外婆说'安抚，拆解具体风险。"

        full_system = f"{cfg.system_prompt}\n\n{emotion_instruction}\n\n【参考资料】\n{context}"

        return {
            "system_prompt": full_system,
            "temperature": cfg.temperature,
            "persona_name": cfg.name_cn,
        }


